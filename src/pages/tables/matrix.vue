<script setup lang='ts'>
interface ListItem {
  name: OperatorName
  index: number
}
const selectedList = ref<ListItem[]>([])
const { data } = useApi<number[][]>('/get_operators_1v1_matrix').post().json()

const labels = computed(() => {
  return [
    {
      text: '',
      key: 'names',
    },
    ...selectedList.value.map((el) => {
      return {
        text: el.name,
        key: el.name,
      }
    }),
  ]
})

const showData = computed(() => {
  const displayData: Record<string, (number | string)[]> = {
    names: selectedList.value.map(el => el.name),
  }

  if (!data.value) {
    return displayData
  }

  const selected = selectedList.value
  selected.forEach(({ name, index: col }) => {
    displayData[name] = selected.map(({ index: row }) => data.value[row][col] / 100)
  })

  return displayData
})
</script>

<template>
  <div size-full flex p-4 gap-4>
    <MatrixTransfer v-model="selectedList" p-4 rounded un-border="~ slate-400" />

    <div
      flex-1
      overflow-auto
      h-full
      rounded
      p-4
      bg-gradient-to-lt
      from-indigo-300
      un-border="~ slate-400"
    >
      <Table :labels="labels" :data="showData" idkey="names" title="1v1 矩阵" />
    </div>
  </div>
</template>
